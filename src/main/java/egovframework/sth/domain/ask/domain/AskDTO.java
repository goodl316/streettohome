package egovframework.sth.domain.ask.domain;

public class AskDTO {
	
	private int ak_no;
	private String ak_name;
	private String ak_pw;
	private String ak_title;
	private String ak_ctnt;
	private String ak_dt;
	private int ak_idx;
	private int ak_dept;
	
	public int getAk_no() {
		return ak_no;
	}
	public void setAk_no(int ak_no) {
		this.ak_no = ak_no;
	}
	public String getAk_name() {
		return ak_name;
	}
	public void setAk_name(String ak_name) {
		this.ak_name = ak_name;
	}
	public String getAk_pw() {
		return ak_pw;
	}
	public void setAk_pw(String ak_pw) {
		this.ak_pw = ak_pw;
	}
	public String getAk_title() {
		return ak_title;
	}
	public void setAk_title(String ak_title) {
		this.ak_title = ak_title;
	}
	public String getAk_ctnt() {
		return ak_ctnt;
	}
	public void setAk_ctnt(String ak_ctnt) {
		this.ak_ctnt = ak_ctnt;
	}
	public String getAk_dt() {
		return ak_dt;
	}
	public void setAk_dt(String ak_dt) {
		this.ak_dt = ak_dt;
	}
	public int getAk_idx() {
		return ak_idx;
	}
	public void setAk_idx(int ak_idx) {
		this.ak_idx = ak_idx;
	}
	public int getAk_dept() {
		return ak_dept;
	}
	public void setAk_dept(int ak_dept) {
		this.ak_dept = ak_dept;
	}
	
	
}

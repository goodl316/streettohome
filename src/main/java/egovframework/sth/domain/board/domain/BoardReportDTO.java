package egovframework.sth.domain.board.domain;

public class BoardReportDTO {
	private int rp_no;
	private int m_no;
	private int b_no;
	private String rp_ctnt;
	
	public int getRp_no() {
		return rp_no;
	}
	public void setRp_no(int rp_no) {
		this.rp_no = rp_no;
	}
	public int getM_no() {
		return m_no;
	}
	public void setM_no(int m_no) {
		this.m_no = m_no;
	}
	public int getB_no() {
		return b_no;
	}
	public void setB_no(int b_no) {
		this.b_no = b_no;
	}
	public String getRp_ctnt() {
		return rp_ctnt;
	}
	public void setRp_ctnt(String rp_ctnt) {
		this.rp_ctnt = rp_ctnt;
	}
	
	
}
